package de.awacademy.weblogTilLeif.beitrag;


public class ArticleDTO {

private String title;
private String text;

	public ArticleDTO() {
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
